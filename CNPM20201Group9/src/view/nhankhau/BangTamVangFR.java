    private void huytrangthaibuttonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_huytrangthaibuttonActionPerformed
        // TODO add your handling code here:
        try{
            DefaultTableModel df=(DefaultTableModel) DSTamVangTable.getModel();
            int index=DSTamVangTable.getSelectedRow();
            String sohokhau = (String)df.getValueAt(index, 1);
            String madinhdanh = (String)df.getValueAt(index, 2);
            String magiaytamvang = (String)df.getValueAt(index, 0);
            int i = tamVangControl.capNhatTinhTrangTamVang("Hoàn thành tạm vắng",magiaytamvang);
            if(i==1){
                nhanKhauService.capNhatTrangThai("Thường trú",sohokhau, madinhdanh);
                JOptionPane.showMessageDialog(this, "Cập nhật tình trạng thành công !");
            }else{
                JOptionPane.showMessageDialog(this, "Cập nhật thất bại !");
            }
        }catch(Exception ex){
            JOptionPane.showMessageDialog(this, ex);
        }

    }//GEN-LAST:event_huytrangthaibuttonActionPerformed

