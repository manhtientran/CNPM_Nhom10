    private void DSTamVangTableMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_DSTamVangTableMouseClicked
        // TODO add your handling code here:
        ThongtinPanel.setVisible(true);
        try{
            DefaultTableModel df=(DefaultTableModel) DSTamVangTable.getModel();
            int index=DSTamVangTable.getSelectedRow();
            String magiaytamvang = (String)df.getValueAt(index, 0);
            tamVangControl.hienThiChiTiet(magiaytamvang, labelNoiTamTru, labelTuNgay, labelDenNgay, labelLyDo);
            if(((String)df.getValueAt(index, 0)).equals("Hoàn thành tạm vắng")){
                huytrangthaibutton.setEnabled(false);
            }
        }catch(Exception ex){
            ex.printStackTrace();
        }
    }//GEN-LAST:event_DSTamVangTableMouseClicked

